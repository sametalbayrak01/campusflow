const storageKey = 'campusflow.locale'
const storedLocale = localStorage.getItem(storageKey)
const initialLocale: Locale = storedLocale === 'en' || storedLocale === 'tr' ? storedLocale : 'tr'

void i18n.use(initReactI18next).init({
  resources: {
    en: { translation: en },
    tr: { translation: tr },
  },
  lng: initialLocale,
  fallbackLng: 'en',
  interpolation: { escapeValue: false },
})

const syncDocumentLanguage = (locale: string) => {
  document.documentElement.lang = locale
  document.title = locale === 'tr'
    ? 'CampusFlow — Öğrenci planlayıcı'
    : 'CampusFlow — Student planner'
}

syncDocumentLanguage(initialLocale)
i18n.on('languageChanged', (locale) => {
  localStorage.setItem(storageKey, locale)
  syncDocumentLanguage(locale)
})

export const changeLocale = (locale: Locale) => i18n.changeLanguage(locale)

export default i18n
